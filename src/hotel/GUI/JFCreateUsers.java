    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        lblBusy.setVisible(true);
        lblBusy.setBusy(true);
        
        SwingWorker worker = new SwingWorker() {

            @Override
            protected Object doInBackground() throws Exception {
                username = txtName.getText();
                email = txtMail.getText();
                accessLevel = cboAccess.getSelectedItem().toString();                               
                
                //Validate Input
                if(!isTextFieldEmpty()){
                    if(txtConfirm.getPassword() != txtPass.getPassword()){                       
                        HError.showErrorMessage("The passwords you entered dont match", parent);
                    }else{  
                        if(hasValue("AccessLevel","Administrator",false) && "Administrator".equals(accessLevel)){
                            HError.showErrorMessage("You cannot be the administrator. An administrator already exists", parent);
                        }else{
                            if(hasValue("Username",username,false)){
                                HError.showErrorMessage("Username already exists", parent);
                            }else{
                                if(hasValue("Email", email,false)){
                                    HError.showErrorMessage("Email you entered already exists", parent);
                                }else{
                                    createUser(username, email,new String(txtPass.getPassword()), accessLevel);
                                    clear();
                                }
                            }                       
                        }
                    }
                }else{
                   JOptionPane.showMessageDialog(parent,"Please fill all available fields.","Empty Field(s)",JOptionPane.INFORMATION_MESSAGE);
                }
             return null;
            }

            @Override
            protected void done() {
                super.done();
                lblBusy.setBusy(false);
                lblBusy.setVisible(false);
            }
            
            
        };
        
        worker.execute();
    }//GEN-LAST:event_btnSaveActionPerformed

