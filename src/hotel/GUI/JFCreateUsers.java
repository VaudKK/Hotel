   private boolean hasValue(String columnName,Object value,boolean caseSensitive){
        boolean hasVal = false;
        
        try {
            hasVal = HotelDB.isRowAvailable(HotelDB.USERS_TABLE,columnName,value,caseSensitive);
        } catch (SQLException ex) {
            
        }        
        return hasVal;
    }
    
