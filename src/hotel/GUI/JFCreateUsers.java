    private void createUser(String username,String email,String secret,String accessLevel){
        
        try{
             
           
            btnSave.setEnabled(false);
            btnCancel.setEnabled(false);
            
            HashMap<String,Object> user = new HashMap<>();
            user.put("Username", username);
            user.put("Email", email);
            user.put("Password", secret);
            user.put("AccessLevel",accessLevel);
            
            DateFormat dateFormat = new SimpleDateFormat(HotelDB.getDateTimeFormat());               
            user.put("DateCreated",dateFormat.format(HotelDB.getCurrentDateTime()));
            
            HotelDB.insertTODB("users", user);
            
            JOptionPane.showMessageDialog(this,username + " has been created successfully.","Saved",JOptionPane.INFORMATION_MESSAGE);
            reset();
               
           }catch(Exception ex){
               HError.showErrorMessage("Error while creating user", this, ex.getMessage());
           }finally{ 
                btnSave.setEnabled(true);
                btnCancel.setEnabled(true);
               setCursor(Cursor.getDefaultCursor());
           }
    }
    
    private boolean isTextFieldEmpty(){
        
        boolean isEmpty = false;
        
        for(Component c : this.getRootPane().getContentPane().getComponents()){
            if(c instanceof JTextField){
                if(((JTextField)c).getText().length() == 0){
                    isEmpty = true;
                }
            }
        }
        
        
        return isEmpty;
    }
    
    private void reset(){
        JTextField field = new JTextField();
        for(Component component : this.getComponents()){
            if(component.equals(field)){
                ((JTextField)component).setText("");
            }
        }
    }
    

